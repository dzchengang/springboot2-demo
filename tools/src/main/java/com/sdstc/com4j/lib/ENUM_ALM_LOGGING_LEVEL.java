package com.sdstc.com4j.lib  ;

import com4j.*;

/**
 * <p>
 * Logging level types
 * </p>
 */
public enum ENUM_ALM_LOGGING_LEVEL {
  /**
   * <p>
   * No logging
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  LOGGING_NONE, // 0
  /**
   * <p>
   * Only error logging
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  LOGGING_ERROR, // 1
  /**
   * <p>
   * warnings and errors logging
   * </p>
   * <p>
   * The value of this constant is 2
   * </p>
   */
  LOGGING_WARNING, // 2
  /**
   * <p>
   * flow logging
   * </p>
   * <p>
   * The value of this constant is 3
   * </p>
   */
  LOGGING_FLOW, // 3
  /**
   * <p>
   * debug logging
   * </p>
   * <p>
   * The value of this constant is 4
   * </p>
   */
  LOGGING_DEBUG, // 4
}
